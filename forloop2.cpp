#include <iostream>
using namespace std;

int main()
{
    // local variable initialization
    int person = 50;
    // output to user
    cout << "Here are your numbers: ";
    cin >> person;
    // execute for loop -> init, condition; increment
    for (int zxc = person; zxc > person; zxc++)
    {
        cout << person << zxc + 1 << endl;

    }

    return 0;

}